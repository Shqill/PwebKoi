                        Awal mula budidaya Koi dilakukan oleh peternak ikan di daerah Sukabumi Jawa Barat sekitar tahun 1982. Kemudian pada tahun 1986, ada seorang petani membawa sejumlah bibit Koi ke daerah Blitar untuk dibudidayakan. Sejak saat itulah Koi mulai ramai dibudidayakan di kedua daerah tesebut. Popularitas Koi sebagai ikan penghias kolam membuat harganya menjadi makin mahal, sempat menggeser ikan Mas dan ikan Gurami sebagai produk utama perikanan air tawar. Para petani ikan konsumsi kemudian beralih membudidayakan Koi sebagai sumber penghasilan mereka. Hingga saat ini, sentra budidaya Koi terutama berada di Blitar dan Sukabumi, juga daerah lainnya seperti Banjarnegara, Cianjur, Bandung (Jawa Barat), Klaten, Magelang (Jawa tengah), Magetan, Kediri, Tulung Agung (Jawa Timur) dan daerah lainnya di pulau Jawa. Kian hari, kualitas Koi yang diproduksi oleh para pembudidaya Koi di daerah tersebut makin meningkat, terutama Koi yang berasal dari daerah Blitar yang telah mampu bersaing dengan Nishikigoi (Koi eks impor Jepang) pada berbagai acara Kontes Koi (Koi Show).
