            Ikan Koi
            <div class="container1">
                <p>
                    Ikan koi adalah jenis ikan hias yang   berasal dari Jepang. Mereka adalah varietas dari ikan mas biasa, dan dikenal karena warna dan pola yang indah. Ikan koi adalah pilihan populer untuk kolam halaman belakang dan taman air, dan juga sering ditemukan di taman umum dan fitur air dekoratif lainnya. Ikan koi hadir dalam berbagai warna, termasuk putih, hitam, merah, kuning, biru, dan oranye, dan dapat memiliki berbagai pola pada sisik mereka. Pola ini bisa berupa bintik-bintik atau garis-garis sederhana hingga desain rumit yang menyerupai bunga atau objek alami lainnya. <br>
                    Ikan koi dianggap sebagai simbol keberuntungan, kemakmuran, dan ketekunan dalam budaya Jepang. Mereka sering muncul dalam seni dan sastra, dan terkadang diberikan sebagai hadiah untuk merayakan acara atau tonggak penting. Selain daya tarik estetika mereka, ikan koi juga dikenal karena kekuatan dan adaptabilitas mereka. Mereka dapat bertahan dalam berbagai kondisi air, dan dapat berkembang baik dalam lingkungan dalam maupun luar ruangan. Mereka juga relatif mudah dirawat, dan dapat diberi makan berbagai makanan termasuk pelet, serpihan, dan makanan hidup atau beku.
