            Tipe Pemelihara Koi di Indonesia
            <div class="container1">
                <p>
                    1) Pembudidaya Koi <br>
                    Seorang pembudidaya Koi, tentu memiliki banyak pengalaman terkait proses pemijahan, perawatan burayak sampai pembesaran Koi. Mereka merawat pasangan induk Koi seperti anak-anak mereka sendiri, Memberi pakan yang baik, merawat kolam dengan hati-hati dengan harapan agar nantinya, dapat menghasilkan keturunan yang baik. <br>
                    2) Pemelihara Koi Kecil <br>
                    Ada pula pecinta Koi yang tak mau repot dengan urusan pemijahan. Mungkin saja karena keterbatasan lahan, waktu dan dana. Ada yang bertujuan bisnis dan ada pula yang sekadar untuk dipelihara di rumah sebagai penghias kolam. <br>
                    3) Pemelihara Koi yang Belum Finished <br>
                    Tipe pecinta Koi ini selalu penasaran mencari dan memelihara Koi yang memiliki pola dan warna yang masih mengalami perkembangan dan perubahan (belum ‘finished’). Pecinta Koi tipe ini rela menunggu 2 atau 3 tahun lagi demi untuk menyaksikan Koi miliknya benar-benar memiliki pola dan warna yang bagus. <br>
                    4) Pemelihara Koi Finished <br>
                    Pecinta Koi tipe ini hanya ingin memelihara Koi yang sudah finish yaitu Koi yang pola dan warnanya relatip tetap (tidak berubah). Biasanya koleksi mereka adalah Koi-Koi juara yang telah memenangkan banyak penghargaan di berbagai acara Kontes Koi (Koi Show).
